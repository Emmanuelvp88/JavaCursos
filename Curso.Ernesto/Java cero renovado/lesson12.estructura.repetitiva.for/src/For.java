public class For {
   public static void main(String [] args){
       //Vamos aver como funcionan los bucles for

       for(int i = 0; i <= 10; i++){
           System.out.print(i + ",") ;
       }/*La variable "i" se declara dentro del bucle por temas de alcannce de Scoup, esto para que
       la variable solo sea valida dentro del este "for"*/

    }
}
